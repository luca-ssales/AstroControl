// Aplica a tradução para todos os elementos na página
function applyTranslations(lang) {
    // 1. Textos simples (innerText)
    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // 2. Placeholders de input
    document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
        const key = element.getAttribute("data-i18n-placeholder");
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute("placeholder", translations[lang][key]);
        }
    });
}

// Inicializa a tradução no carregamento da página
document.addEventListener("DOMContentLoaded", () => {
    const activeLang = localStorage.getItem("lang") || "pt";
    applyTranslations(activeLang);
    
    // Atualiza o botão de idioma inicial
    updateLangButtonUI(activeLang);
});

// Altera o idioma e salva a preferência
function changeLanguage(lang) {
    localStorage.setItem("lang", lang);
    applyTranslations(lang);
    updateLangButtonUI(lang);
}

// Atualiza a aparência do botão de idioma
function updateLangButtonUI(lang) {
    const langBtn = document.getElementById("lang-toggle");
    if (!langBtn) return;
    
    if (lang === "pt") {
        langBtn.innerHTML = "🌐 EN";
        langBtn.title = "Switch to English";
    } else {
        langBtn.innerHTML = "🌐 PT";
        langBtn.title = "Mudar para Português";
    }
}

// Função toggle rápida para o botão
function toggleLanguage() {
    const currentLang = localStorage.getItem("lang") || "pt";
    const nextLang = currentLang === "pt" ? "en" : "pt";
    changeLanguage(nextLang);
}
